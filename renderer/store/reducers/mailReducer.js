export const mailReducer = (state = defaultState, action) => {
  switch (action.type) {
    case 'FILTER_MAILS':
      return {
        ...state,
        mails: action.mails,
      }
    case 'SET_MAIL_LIST':
      return {
        ...state,
        active: action.active,
      }
    case 'SET_ACTIVE_MAIL':
      return {
        ...state,
        activeMail: action.activeMail,
      }
    default:
      return state
  }
}